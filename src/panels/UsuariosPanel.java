    public static void reiniciarDatos(String buscarCodigo){
       
        llenarTabla("");
        
        for(int i=0; i<tabla.getRowCount(); i++){
            if(tabla.getValueAt(i, 0).equals(buscarCodigo)){
               tabla.changeSelection(i, 0, false, false);
            }
        }
         estadoCombo.setSelectedIndex(0);
         nombreTxt.setText("");
    }
    
