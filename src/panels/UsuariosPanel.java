public static void llenarTabla() {

        try (Connection con = new ConectarCloudcPanel("corporacionagama_mainbigmedic").getCon()) {

            String sql = "call `sp.ConsAllusers` (?)";
            PreparedStatement pst = con.prepareCall(sql);
            pst.setString(1, estadox);
            ResultSet rs = pst.executeQuery();
            vaciarTabla();
            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            todos=0;
            activos=0;
            bloqueados=0;
            while (rs.next()) {
                todos++;
                String cod = rs.getString("codigo");
                String est = rs.getString("estado");
                if(est.toLowerCase().contains("activ")){
                    activos++;
                }else{
                    bloqueados++;
                }
                
                modelo.addRow(new Object[]{
                    
                    rs.getString("id"),
                    cod, rs.getString("nombre"), 
                    rs.getString("estado")
                    
                    
                    
                });

            }

            if (modelo.getRowCount() == 0) {
                modelo.addRow(new Object[]{
                    "", "Sin resultados"
                });
            }
        } catch (Exception e) {
            Logger.getLogger(UsuariosPanel.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Error con el manejo de base de datos, contacte con el adm.\n" + e);
        }
        totalUserTxt.setText(todos+"");
        userActivosTxt.setText(activos+"");
        userBloqueadosTxt.setText(bloqueados+"");
    }

